    private void btnIssueLicenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueLicenseActionPerformed
    int selectedRow = tblOpenRequest.getSelectedRow();
    if(selectedRow>=0){
    WorkRequest wr = (WorkRequest)tblOpenRequest.getValueAt(selectedRow, 4);
    IssueGunLicenseJPanel panel = new IssueGunLicenseJPanel(userProcessContainer, wr, business);
    userProcessContainer.add("IssueGunLicenseJPanel",panel);
    CardLayout layout = (CardLayout)userProcessContainer.getLayout();
    layout.next(userProcessContainer);
    }else{
    JOptionPane.showMessageDialog(null, "Please select any row");
    }
    }//GEN-LAST:event_btnIssueLicenseActionPerformed

