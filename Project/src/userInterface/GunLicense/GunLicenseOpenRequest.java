    public void populateTable(){
    DefaultTableModel dtm = (DefaultTableModel)tblOpenRequest.getModel();
    dtm.setRowCount(0);
    for(WorkRequest wr : organization.getWorkQueue().getWorkRequest()){
    if(wr.getMessage().equalsIgnoreCase("Pending with Gun License Department")){
    Object[] row = new Object[6];
    row[0] = wr.getNo();
    row[1] = wr.getFireArms().getGunType();
    row[2] = wr.getFireArms().getModel();
    row[3] = wr.getSender().getCustomer().getSsn();
    row[4] = wr;
    row[5] = wr.getMessage();
    dtm.addRow(row);
    }}
    }

