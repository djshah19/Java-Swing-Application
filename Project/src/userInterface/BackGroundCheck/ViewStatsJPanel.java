    private void btnGunViolenceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGunViolenceActionPerformed
    ArrayList<String> yearList = new ArrayList<>();
    DefaultCategoryDataset dcd = new DefaultCategoryDataset();
    int counter;
    for(Criminal criminal : business.getCriminalDirectory().getCriminalList()){
    if(!yearList.contains(criminal.getYear())){
    yearList.add(String.valueOf(criminal.getYear()));
    }
    }
    for(String yr : yearList){
    counter = 0;
    for(Criminal criminal : business.getCriminalDirectory().getCriminalList()){
    if(yr.equalsIgnoreCase(String.valueOf(criminal.getYear())) && 
            criminal.getCrimeType().equalsIgnoreCase("Gun Violence")){
    counter = counter + 1;
    }
    }
    dcd.setValue(counter, "NoOfGunViolence", yr);
    }
    JFreeChart chart = ChartFactory.createBarChart("Gun Violence Reported in area " + workrq.getSender().getCustomer().getZip(), 
            "Year", "No of Gun Violence", dcd, PlotOrientation.VERTICAL, false, true, false);
    CategoryPlot p = chart.getCategoryPlot();
    p.setRangeGridlinePaint(Color.black);
    ChartFrame frame = new ChartFrame("Bar Chart", chart);
    frame.setVisible(true);
    frame.setSize(450,350);
    }//GEN-LAST:event_btnGunViolenceActionPerformed

