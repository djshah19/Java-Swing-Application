    private void btnGunOwnersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGunOwnersActionPerformed
        ArrayList<String> yearList = new ArrayList<>();
        DefaultCategoryDataset dcd = new DefaultCategoryDataset();
        int counter;
        for(GunLicense gl : business.getGunLicenseDirectory().getGunLicenseList()){
            System.out.println(gl.getValidFrom().substring(23, 28));
            if(!yearList.contains(gl.getValidFrom().substring(23, 28)) &&
                workrq.getSender().getCustomer().getZip()==gl.getOwner().getZip()){
                yearList.add(String.valueOf(gl.getValidFrom().substring(23, 28)));
            }
        }
        for(String yr : yearList){
            counter = 0;
            for(GunLicense gl : business.getGunLicenseDirectory().getGunLicenseList()){
                if(yr.equalsIgnoreCase(String.valueOf(gl.getValidFrom().substring(23, 28)))
                    && workrq.getSender().getCustomer().getZip()==gl.getOwner().getZip()){
                    counter = counter + 1;
                }
            }
            dcd.setValue(counter, "NoOfGunOwner(s)", yr);
        }
        JFreeChart chart = ChartFactory.createBarChart("Gun Owner(s) in area " + workrq.getSender().getCustomer().getZip(),
            "Year", "No of Gun Owner(s)", dcd, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.black);
        ChartFrame frame = new ChartFrame("Bar Chart", chart);
        frame.setVisible(true);
        frame.setSize(450,350);
    }//GEN-LAST:event_btnGunOwnersActionPerformed

    private void btnWeaponsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWeaponsActionPerformed
        ArrayList<String> weaponsList = new ArrayList<>();
        DefaultCategoryDataset dcd = new DefaultCategoryDataset();
        int counter;
        for(Criminal criminal : business.getCriminalDirectory().getCriminalList()){
            if(!weaponsList.contains(criminal.getWeaponUsed()) &&
                workrq.getSender().getCustomer().getZip()==criminal.getZip()){
                weaponsList.add(criminal.getWeaponUsed());
            }
        }
        for(String weapon : weaponsList){
            counter = 0;
            for(Criminal criminal : business.getCriminalDirectory().getCriminalList()){
                if(weapon.equalsIgnoreCase(criminal.getWeaponUsed()) &&
                    criminal.getCrimeType().equalsIgnoreCase("Gun Violence")
                    && workrq.getSender().getCustomer().getZip()==criminal.getZip()){
                    counter = counter + 1;
                }
            }
            dcd.setValue(counter, "NoOfTimes", weapon);
        }
        JFreeChart chart = ChartFactory.createBarChart("Weapons used for Violence in area " + workrq.getSender().getCustomer().getZip(),
            "Weapons", "No of Times", dcd, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.black);
        ChartFrame frame = new ChartFrame("Bar Chart", chart);
        frame.setVisible(true);
        frame.setSize(450,350);
    }//GEN-LAST:event_btnWeaponsActionPerformed


