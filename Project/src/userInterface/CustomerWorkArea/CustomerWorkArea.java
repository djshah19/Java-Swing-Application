    public void populateTable(){
    DefaultTableModel dtm = (DefaultTableModel)tblItemDet.getModel();
    dtm.setRowCount(0);
    for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
    if(enterprise instanceof GunSeller){
    for(Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
    if(org instanceof MaterialOrganization){
    for(FireArms fa : org.getFireArmsDirectory().getFireArmsList()){
    Object[] row = new Object[6];
    row[0] = enterprise;
    row[1] = fa.getGunType();
    row[2] = fa;
    row[3] = fa.getSku();
    row[4] = fa.getPrice();
    row[5] = fa.getQuantity()>0?"In Stock":"Out of Stock";
    dtm.addRow(row);
    }
    }
    }
    }
    }
    }
