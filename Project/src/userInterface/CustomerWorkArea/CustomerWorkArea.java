    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblItemDet.getSelectedRow();
        if(selectedRow>=0){
        Organization orgSales = null;
        Random random = new Random();
        int no = random.nextInt(5000) + 1;
        FireArms fireArms = (FireArms)tblItemDet.getValueAt(selectedRow, 2);
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
        if(organization instanceof SalesOrganization){
            orgSales = organization;
        }else if(organization instanceof BackGroundCheck){
        orgBkgdck = organization;
        }
            
        }}
        SalesWorkRequest request = new SalesWorkRequest();
        request.setNo(no);
        request.setFireArms(fireArms);
        request.setSender(account);
        request.setStatus("Under Process");
        request.setMessage("Pending with police for background verification");
        orgSales.getWorkQueue().getWorkRequest().add(request);
        account.getWorkQueue().getWorkRequest().add(request);
        orgBkgdck.getWorkQueue().getWorkRequest().add(request);
        JOptionPane.showMessageDialog(null, "Order placed successfully!" + "\n" + "Please complete the risk assessment.");
        SendMail.send("shah.dhw@husky.neu.edu", "We've received your order #" + no, 
        "Hi "+account.getCustomer().getfName()+","+'\n'+'\n'+
         "Thank you for shopping with us."+"\n"+'\n'+"You Ordered "+fireArms.getGunType() 
                + " " + fireArms.getModel() + " " + fireArms.getPrice()+
                '\n'+'\n'+"Please complete the risk assessment."
                , "dhwanishah781@gmail.com", "9769402842");
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Select any Row");
        }
    }//GEN-LAST:event_btnBookActionPerformed

