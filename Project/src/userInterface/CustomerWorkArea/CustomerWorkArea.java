    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
    DefaultTableModel dtm = (DefaultTableModel)tblItemDet.getModel();
    if(!txtGunSeller.getText().isEmpty() && !txtGunType.getText().isEmpty()){
    for(int row = 0; row < dtm.getRowCount(); row++){
    Enterprise enterprise = (Enterprise)tblItemDet.getValueAt(row, 0);
    FireArms fa = (FireArms)tblItemDet.getValueAt(row, 2);
    if(!enterprise.getName().equalsIgnoreCase(txtGunSeller.getText()) && 
       !fa.getGunType().equalsIgnoreCase(txtGunType.getText())){
    dtm.removeRow(row);
    row--;
    }
    }
    }else if(!txtGunSeller.getText().isEmpty() && txtGunType.getText().isEmpty()){
    for(int row = 0; row < dtm.getRowCount(); row++){
    Enterprise enterprise = (Enterprise)tblItemDet.getValueAt(row, 0);
    if(!enterprise.getName().equalsIgnoreCase(txtGunSeller.getText())){
    dtm.removeRow(row);
    row--;
    }}
    }else if(txtGunSeller.getText().isEmpty() && !txtGunType.getText().isEmpty()){
    for(int row = 0; row < dtm.getRowCount(); row++){
    FireArms fa = (FireArms)tblItemDet.getValueAt(row, 2);
    if(!fa.getGunType().equalsIgnoreCase(txtGunType.getText())){
    dtm.removeRow(row);
    row--;
    }}
    }      
    }//GEN-LAST:event_btnSearchActionPerformed

