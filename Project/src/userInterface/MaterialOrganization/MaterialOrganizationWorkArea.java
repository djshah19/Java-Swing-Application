     public void populateTable(){
     DefaultTableModel dtm = (DefaultTableModel)tblInventory.getModel();
     dtm.setRowCount(0);
    for(FireArms fireArms : organization.getFireArmsDirectory().getFireArmsList()){
    Object[] row = new Object[5];
    row[0] = fireArms.getGunType();
    row[1] = fireArms;
    row[2] = fireArms.getSku();
    row[3] = fireArms.getPrice();
    row[4] = fireArms.getQuantity();
    dtm.addRow(row);
    }
     }
