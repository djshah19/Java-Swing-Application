    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
    if(txtSku.getText().isEmpty()){
     JOptionPane.showMessageDialog(null, "Please enter Sku to search",
     "Error", JOptionPane.ERROR_MESSAGE);
    }else{
    FireArms fa = organization.getFireArmsDirectory().searchFireArms(Integer.parseInt(txtSku.getText()));
    if(fa==null){
     JOptionPane.showMessageDialog(null, "Invalid Sku",
     "Error", JOptionPane.ERROR_MESSAGE);
     return;
    }
    UpdateFireArms panel = new UpdateFireArms(userProcessContainer, fa);
    userProcessContainer.add("UpdateFireArms", panel);
    CardLayout layout = (CardLayout)userProcessContainer.getLayout();
    layout.next(userProcessContainer);
    }     
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdActionPerformed
            int selectedRow = tblInventory.getSelectedRow();
    if(selectedRow>=0){
        FireArms fa = (FireArms)tblInventory.getValueAt(selectedRow, 1);
        UpdateFireArms panel = new UpdateFireArms(userProcessContainer, fa);
        userProcessContainer.add("UpdateFireArms", panel);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }else{
        JOptionPane.showMessageDialog(null, "Please Select any Row");
        }  
    }//GEN-LAST:event_btnUpdActionPerformed


